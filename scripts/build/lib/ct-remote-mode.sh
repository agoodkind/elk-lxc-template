# Remote mode build function for CT wrapper
# Creates CT wrapper for ProxmoxVE community scripts submission
# Usage: Production mode - relies on ProxmoxVE infrastructure

# ----------------------------------------------------------------------------
# Function: build_remote_mode
# Description: Creates CT wrapper for ProxmoxVE submission
# 
# Flow:
#   1. CT wrapper sources build.func from ProxmoxVE main
#   2. Calls build_container() which downloads installer from ProxmoxVE:
#      https://raw.githubusercontent.com/community-scripts/ProxmoxVE/main/install/elk-stack-install.sh
#   3. That installer MUST be self-contained (generated by installer.sh in non-LOCAL mode)
#
# Requires: OUT_FILE, PROXMOX_REPO_URL set by caller
# ----------------------------------------------------------------------------
build_remote_mode() {
    echo "Building CT wrapper (ProxmoxVE submission mode)..."
    echo "  → ProxmoxVE framework: $PROXMOX_REPO_URL"
    echo "  → Installer: ProxmoxVE will serve from install/elk-stack-install.sh"
    
    # Generate URL for ProxmoxVE framework
    local build_func_source="source <(curl -fsSL $PROXMOX_REPO_URL/misc/build.func)"
    
    # Assemble the final CT wrapper script
    {
        # PART 1: Shebang and build.func source
        echo "#!/usr/bin/env bash"
        echo "$build_func_source"
        
        # PART 2: CT wrapper (variables, metadata, functions, build_container call)
        # build_container() in build.func automatically downloads installer from ProxmoxVE
        # No manual installer execution needed - framework handles it via var_install variable
        sed -n '/{{BUILD_FUNC_SOURCE}}/,/{{INSTALL_SCRIPT_OVERRIDE}}/p' \
            templates/elk-stack-ct-content.sh | sed '1d;$d'
        
        # PART 3: CT wrapper footer (success messages, final instructions)
        sed -n '/{{INSTALL_SCRIPT_OVERRIDE}}/,$p' templates/elk-stack-ct-content.sh | tail -n +2
    } > "$OUT_FILE"
}

